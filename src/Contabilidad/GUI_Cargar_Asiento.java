    /**
     * Nos indica true si es una cuenta, false si es un titulo
     * @param nroCuenta
     * @return true si es cuenta, false si es titulo
     */
    private boolean esImputable(String nroCuenta){
        if(!nroCuenta.equals("")){            
            cuenta=obtenerCuenta(nroCuenta+"");
            return cuenta.isImputable_C();
        }        
        else
            return false;
    }
    
    
