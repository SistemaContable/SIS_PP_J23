    private boolean camposNecesarios () {
        if ((jTextField1.getText().length()==0) ||
            (jTextField2.getText().length()==0) ||
            (jTextField3.getText().length()==0) ||
            (jTextField4.getText().length()==0) ||
            (jTextField5.getText().length()==0) ||
            (jTextField6.getText().length()==0))
        {
            JOptionPane.showMessageDialog(null, "Complete todos los campos!","Atención",JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;        
    }
    
   
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        r_con.Connection();
        if (("Aceptar".equals(this.jButton2.getText())) && (camposNecesarios())){            
            //r_con.Connection(); 
            String sql = "INSERT INTO Articulos "
                        + "VALUES('"+jTextField1.getText()+"','"
                                    +jTextField2.getText()+"','"
                                    +jTextField3.getText()+"',"
                                    +Float.parseFloat(jTextField4.getText())+","
                                    +Integer.parseInt(jTextField5.getText())+",'"
                                    +jTextField6.getText()+"')";
           
            if (r_con.Insertar(sql)){
                // para auditoria                
                Auditoria auditoria = new Auditoria(r_con);
                auditoria.insertarArticulo(usuario.getUsuario(),jTextField1.getText(),jTextField2.getText(),jTextField3.getText(),jTextField4.getText(),jTextField5.getText(),jTextField6.getText(),"Alta");                                
                limpiarForm();
            }
            //r_con.cierraConexion();            
        }
        else{
            if (("Buscar".equals(this.jButton2.getText()))){                
                boolean existe = false;               
                //r_con.Connection();
                ResultSet rs = r_con.Consultar("SELECT * FROM Articulos WHERE art_codigo = '"+jTextField1.getText()+"'");
                try {                     
                     //while (rs.next())
                    //{     
                    
                    if (rs.next()){
                    if ("Baja Artículo:".equals(jLabel1.getText())){
                            jTextField1.setEnabled(false);
                        }
                        if ("Modificación Artículo:".equals(jLabel1.getText())){
                            this.form_Complete();
                        }
                        if ("Consulta Artículo:".equals(jLabel1.getText())){
                            this.form_Complete();
                        }
                      
                        jTextField2.setText(rs.getString(2));
                        jTextField3.setText(rs.getString(3));
                        jTextField4.setText(rs.getString(4));
                        jTextField5.setText(rs.getString(5));
                        jTextField6.setText(rs.getString(6));
                        muestraValor(jTextField6.getText());
                        
                        existe = true;                        
                    //}
                    } 
                    rs.close();
                } catch (SQLException ex) {
                    Logger.getLogger(GUI_A_Articulo.class.getName()).log(Level.SEVERE, null, ex);
                }                
                //r_con.cierraConexion();
                if (!existe){
                    jTextField1.setEnabled(true);
                    jTextField1.requestFocus();
                    jTextField1.selectAll();
                    JOptionPane.showMessageDialog(null, "El Articulo "+jTextField1.getText()+" no se encuentra registrado.","Cuidado",JOptionPane.WARNING_MESSAGE);
                }
                else{
                    if ("Baja Artículo:".equals(jLabel1.getText())){
                        buttonEliminar ();
                     }
                     if ("Modificación Artículo:".equals(jLabel1.getText())){
                        buttonModificar ();
                     }
                     if ("Consulta Artículo:".equals(jLabel1.getText())){
                         buttonNuevaConsulta();
                     }                    
                }
            }
            else
            {
                if (("Eliminar".equals(this.jButton2.getText()))){               
                
               // r_con.Connection();
                r_con.Borrar("DELETE FROM Articulos WHERE art_codigo = '"+jTextField1.getText()+"'");

                // para auditoria
                                                                              
                Auditoria auditoria=new Auditoria(r_con);
                auditoria.insertarArticulo(usuario.getUsuario(),jTextField1.getText(), jTextField2.getText(), jTextField3.getText(),jTextField4.getText() , jTextField5.getText(), jTextField6.getText(),"Baja");
                                
                limpiarForm();                
                //r_con.cierraConexion();               
                this.dispose();
                }
                else{
                     if (("Nueva Consulta".equals(this.jButton2.getText()))){
                        this.limpiarForm();
                        form_onlySearch();
                        buttonBuscar();
                     }
                     else{
                         if (("Modificar".equals(this.jButton2.getText()))&& (camposNecesarios())){
                                    
                                    //r_con.Connection();                                    
                                    String sql =  "UPDATE Articulos SET "
                                                + "art_desc = '"+jTextField2.getText()+"', "
                                                + "art_proveedor = '"+jTextField3.getText()+"', "
                                                + "art_precio = '"+Float.parseFloat(jTextField4.getText())+"', "
                                                + "art_stock = '"+Integer.parseInt(jTextField5.getText())+"', "
                                                + "art_cod_tasa_iva = '"+jTextField6.getText()+"' WHERE "
                                                + "art_codigo = '"+jTextField1.getText()+"';";
                                    if (r_con.Actualizar(sql)){
                                        Auditoria auditoria=new Auditoria(r_con);
                                        auditoria.insertarArticulo(usuario.getUsuario(),jTextField1.getText(), jTextField2.getText(), jTextField3.getText(),jTextField4.getText() , jTextField5.getText(), jTextField6.getText(),"Modificación");
                                        limpiarForm();
                                        form_onlySearch();        
                                        buttonBuscar();
                                    }
                                    //r_con.cierraConexion();                            
                         }
                     }
                }
            }          
        }
        r_con.cierraConexion();   
    }//GEN-LAST:event_jButton2ActionPerformed

