private boolean muestraValor (String cod){
    boolean existe = false;
    
    r_con.Connection();
    ResultSet rs = r_con.Consultar("SELECT * FROM tipo_tasas_iva WHERE tasa_clave = '"+cod+"'");
    try {
             while (rs.next())
            {
                jLabel8.setText(rs.getString(2));
                existe = true;
            } 
            rs.close();
    } catch (SQLException ex) {            
            Logger.getLogger(GUI_A_Articulo.class.getName()).log(Level.SEVERE, null, ex);
    }
    r_con.cierraConexion();
    return existe;
}
