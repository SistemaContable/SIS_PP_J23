    private void jTextField2FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField2FocusLost
        Validaciones v = new Validaciones();
        String cad = v.limpiarEspacios(jTextField2.getText());
        cad = v.soloPrimerMayus(cad);
        if ((cad.length()<=25)) {
            jTextField2.setText(cad);
            jTextField2.setToolTipText("campo correcto.");
        }
        else{
            jTextField2.requestFocus();
            jTextField2.selectAll();
            jTextField2.setToolTipText("longitud maxima 25 caracteres.");              
            mostrarMSSG(evt.getComponent());
        }
    }//GEN-LAST:event_jTextField2FocusLost

