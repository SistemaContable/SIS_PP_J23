    private boolean camposNecesarios () {
       if ((jTextField1.getText().length()==0) || 
          (jTextField2.getText().length()==0) || 
          (jTextField3.getText().length()==0)) 
       {
            JOptionPane.showMessageDialog(null, "Complete todos los campos!","Atención",JOptionPane.WARNING_MESSAGE);
            return false;
       }
       return true;      
               
    }
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        r_con.Connection();
        if (("Aceptar".equals(this.jButton2.getText())) && (camposNecesarios())){
            
            //r_con.Connection();             
            String sql = "INSERT INTO Tasas_IVA "
                        + "VALUES('"+jTextField1.getText()+"','"
                                    +jTextField2.getText()+"','"
                                    +jTextField3.getText()+"')";
            if (r_con.Insertar(sql)){                
                Auditoria auditoria=new Auditoria(r_con);                   
                auditoria.insertarTasaIva(usuario.getUsuario(),jTextField1.getText(),jTextField2.getText(),jTextField3.getText(),"Alta");                    
                limpiarForm();
            }
            //r_con.cierraConexion();
            
        }
        else{
            if (("Buscar".equals(this.jButton2.getText()))){
                
                boolean existe = false;
                
                //r_con.Connection();
                ResultSet rs = r_con.Consultar("SELECT * FROM Tasas_IVA WHERE tasa_clave = '"+jTextField1.getText()+"'");
                try {
                     
                     if (rs.next())
                    {     
                        if ("Baja Tasa de IVA:".equals(jLabel1.getText())){
                            jTextField1.setEnabled(false);
                        }
                        if ("Modificación Artículo:".equals(jLabel1.getText())){
                            this.form_Complete();
                        }
                        if ("Consulta Artículo:".equals(jLabel1.getText())){
                            this.form_Complete();
                        }
                        
                        jTextField2.setText(rs.getString(2));
                        jTextField3.setText(rs.getString(3));                        
                        
                        existe = true;
                    }
                     rs.close();
                } catch (SQLException ex) {

                    Logger.getLogger(GUI_A_IVA.class.getName()).log(Level.SEVERE, null, ex);
                }
                //r_con.cierraConexion();
                if (!existe){
                    jTextField1.setEnabled(true);
                    jTextField1.requestFocus();
                    jTextField1.selectAll();
                    JOptionPane.showMessageDialog(null, "El Articulo "+jTextField1.getText()+" no se encuentra registrado.","Cuidado",JOptionPane.WARNING_MESSAGE);
                }
                else{
                    if ("Baja Tasa de IVA:".equals(jLabel1.getText())){
                        buttonEliminar ();
                     }
                     if ("Modificación Artículo:".equals(jLabel1.getText())){
                        buttonModificar ();
                     }
                     if ("Consulta Artículo:".equals(jLabel1.getText())){
                         buttonNuevaConsulta();
                     }
                     
                }
            }
            else{
                if (("Eliminar".equals(this.jButton2.getText()))){               
                    
                    //r_con.Connection();
                    Auditoria auditoria=new Auditoria(r_con);                   
                                        
                    r_con.Borrar("DELETE FROM Tasas_IVA WHERE tasa_clave = '"+jTextField1.getText()+"'");                    
                    auditoria.insertarTasaIva(usuario.getUsuario(),jTextField1.getText(),jTextField2.getText(),jTextField3.getText(),"Baja");             
                    this.dispose();
                }
                
            }
          
        }
        r_con.cierraConexion(); 
    }//GEN-LAST:event_jButton2ActionPerformed

